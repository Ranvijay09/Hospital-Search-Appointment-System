    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed

        if (jLabel1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please select post.");
            return;
        }
        int c_id = jComboBox1.getItemAt(this.jComboBox1.getSelectedIndex()).getID();
        String c_name = jTextField3.getText().toString();
        if (c_name.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter new post name.");
            return;
        }
        try {
            MainModule.DB_Creds db = new MainModule.DB_Creds();
            Connection connection = db.ConnectToDB();
            Statement stmt;
            stmt = connection.createStatement();
            int f = stmt.executeUpdate("update posts set post_name='" + c_name + "' where post_id=" + c_id);
            if (f > 0) {
                JOptionPane.showMessageDialog(null, "Post details updated.");
                jTextField3.setText("");
                jLabel1.setText("");
            } else {
                jTextField3.setText("");
                jLabel1.setText("");
            }
            stmt.close();
            connection.close();
        } catch (HeadlessException | NumberFormatException | SQLException e) {
            System.out.println("Exception: " + e);
        }
        dispose();
        MainModule.HREditDelDept aedc = new MainModule.HREditDelDept();
        aedc.setVisible(true);
    }//GEN-LAST:event_jButton9ActionPerformed

