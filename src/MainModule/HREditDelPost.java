    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        if (jLabel1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please select post.");
            return;
        }
        int c_id = jComboBox1.getItemAt(this.jComboBox1.getSelectedIndex()).getID();
        int confirm = JOptionPane.showConfirmDialog(null,
            "Do you want to delete post?", "Confirmation Message", JOptionPane.YES_NO_OPTION);
        if (confirm == 0) {
            try {
                MainModule.DB_Creds db = new MainModule.DB_Creds();
                Connection connection = db.ConnectToDB();
                Statement stmt;
                stmt = connection.createStatement();
                int f = stmt.executeUpdate("delete from posts where post_id=" + c_id);
                if (f > 0) {
                    JOptionPane.showMessageDialog(null, "Post details deleted.");
                    jTextField3.setText("");
                    jLabel1.setText("");
                } else {
                    jTextField3.setText("");
                    jLabel1.setText("");
                }
                stmt.close();
                connection.close();
            } catch (HeadlessException | NumberFormatException | SQLException e) {
                System.out.println("Exception: " + e);
            }
            dispose();
            MainModule.HREditDelDept aedc = new MainModule.HREditDelDept();
            aedc.setVisible(true);
        }
    }//GEN-LAST:event_jButton11ActionPerformed

